# checking data types

# integer data types
print(type(10))                 # class 'int'
print(type(3.14))               # class 'float'
print(type(1 + 3j))             # class Complex
print(type("Emran"))            # class String
print(type([1, 2, 3, 4]))       # class List
print(type({'name': 'Emran'}))  # class Dictionary
print(type({9.8, 3.14, 2.7}))   # class Set
print(type((9.8, 3.14, 2.7)))   # class Tuple
print(type(3 == 3))             # class Bool
print(type(3 >= 3))             # class Bool
